def Neurona(E, I, U):
    #si una inh=1 salida=0
    for inh in I:
        if inh == 1:
            return 0
        
    #sumar exi 
    sum_exi = 0
    for exi in E:
        sum_exi += exi

    #mayor o igual al umbral 
    if sum_exi >= U:
        return 1
    else:
        return 0
    
#PRUEBAS 
print("EJEMPLO 1")
E = [1]
I = [0]
U = 1

salida = Neurona(E, I, U)
print(f"E = {E}, I = {I}, U = {U} salida = {salida}\n")

#EJEMPLO OR
print("EJEMPLO OR (E=[X,Y], U=1, I=[])")
valores = [[0,0], [0,1], [1,0], [1,1]]
for x in valores:
    print(f"E={x}, I=[], U=1 salida={Neurona(x, [], 1)}\n")

#EJEMPLO AND
print("EJEMPLO AND (E=[X,Y], U=2, I=[])")
for x in valores: 
    print(f"E={x}, I=[], U=2 Salida={Neurona(x, [], 2)}\n")

#EJEMPLO NOT 
print("EJEMPLO NOT (E=[1], I=[X], U=1)")
for l in [0, 1]:
    print(f"E=[1], I=[{l}], U=1 salida={Neurona([1], [l], 1)}")
